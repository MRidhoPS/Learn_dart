void main(){
  var nilai = 90;

  switch(nilai){
    case 90:
      print("Wow anda sangat jenius");
      break;
    case 80:
      print('Wow anda lulus');
      break;
    case 70:
      print("wow and tinggal kelas");
      break;

    case 40:
      print('wow anda goblok');  
      break;
    default:
      print('anda bukan manusia');
  }

}

