void main() {
  for (var counter = 1; counter <= 10; counter++) {
    print('counter is $counter');
  }
}

/**
 * for digunakan untuk perulangan
 * blok kode yang ada di dalam for akan terus mengulang selama kondisi for terpenuhi
 * for(init statement; kondisi; post statement){
    blok perulangan}
 * init statement akan dieksekusi sekali diawal
 * kondisi akan dilakukan pengecekan setiap perulangan, jika true perulangan akan terus berjalan jika false maka akan berhenti
 * post statement akan di eksekusi di akhir perulangan
 * 3 parameter tersebut tidak wajib diisi, jika tidak disi maka perulangan akan bersifat true secara menerus   
 */